    private void saveresultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveresultActionPerformed
        // TODO add your handling code here:
        String rollno = roll.getText();
        String physics = phy.getText();
        String maths = math.getText();
        String em = ems.getText();
        String dbms = db.getText();
        String osystem = os.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Host@123#");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'");
            if(rs.next())
            {
                st.executeUpdate("insert into result(rollno, physics, maths, dbms,em,OS) values('"+rollno+"', '"+physics+"', '"+maths+"', '"+em+"', '"+dbms+"', '"+osystem+"')");
                JOptionPane.showMessageDialog(null, "Result Added Successfully");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Student is not Registered");
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_saveresultActionPerformed

